package ua.training.project.model.entity;

public class UserRole {
    private int id;
    private Role name;

    public UserRole(int id, Role name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Role getName() {
        return name;
    }

    public void setName(Role name) {
        this.name = name;
    }
}
